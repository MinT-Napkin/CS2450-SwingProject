    public static void updateScoreLabels() {
        ScoreManager1.setScoreLabel(jLabel4);
        ScoreManager2.setScoreLabel(jLabel5);
        if (ScoreManager1.getScore() == 100) {
            jLabel4.setText("WINS!");
            jLabel5.setText("Loses :(");
            GameManager.setStatus(false);
        } else if (ScoreManager2.getScore() == 100) {
            jLabel5.setText("WINS!");
            jLabel4.setText("Loses :(");
            GameManager.setStatus(false);
        }
    }

